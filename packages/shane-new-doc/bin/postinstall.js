#!/usr/bin/env node

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { input } from "@inquirer/prompts";
import { askLangIfNeeded, makeT, packageDirFromImportMeta } from "../lib/i18n.js";

const projectRoot = process.env.INIT_CWD || process.cwd();
const pkgJsonPath = path.join(projectRoot, "package.json");

const packageDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const templatePath = path.join(packageDir, "bin", "new-doc.js");
const CONFIG_FILE = ".shane-new-doc.json";

const CANDIDATE_NAMES = [
  "new-doc.js",
  "new doc.js",
  "new-docs.js",
  "new docs.js",
  "newdoc.js",
  "newdocs.js",
];

// Marker written at the top of the generated file, used to tell whether this
// file has already been taken over by this package. Files that already carry
// the marker won't be touched again on reinstall, protecting manual edits.
const MANAGED_MARKER = "// @generated by shane-new-doc";

function readProjectPackage() {
  if (!fs.existsSync(pkgJsonPath)) {
    return null;
  }

  try {
    return JSON.parse(fs.readFileSync(pkgJsonPath, "utf8"));
  } catch {
    return null;
  }
}

function readExistingConfig() {
  const configPath = path.join(projectRoot, CONFIG_FILE);
  if (!fs.existsSync(configPath)) return null;
  try {
    return JSON.parse(fs.readFileSync(configPath, "utf8"));
  } catch {
    return null;
  }
}

function isOwnRepository(pkg) {
  return pkg?.name === "shane-new-doc";
}

// Checks whether the target project actually uses @astrojs/starlight.
// The new-doc.js this package generates is hardcoded to Starlight's directory
// layout (src/content/docs) and frontmatter shape (sidebar: order), so
// dropping it into a non-Starlight project would be pointless or misleading.
function isStarlightProject(pkg) {
  if (!pkg) return false;

  const deps = {
    ...(pkg.dependencies || {}),
    ...(pkg.devDependencies || {}),
    ...(pkg.optionalDependencies || {}),
  };

  return Boolean(deps["@astrojs/starlight"]);
}

function findExistingScript(scriptsDir) {
  for (const name of CANDIDATE_NAMES) {
    if (fs.existsSync(path.join(scriptsDir, name))) {
      return name;
    }
  }

  return null;
}

function isAlreadyManaged(filePath) {
  if (!fs.existsSync(filePath)) {
    return false;
  }

  try {
    const firstLines = fs.readFileSync(filePath, "utf8").slice(0, 200);
    return firstLines.includes(MANAGED_MARKER);
  } catch {
    return false;
  }
}

function writeConfig(imgDir, lang) {
  const configPath = path.join(projectRoot, CONFIG_FILE);

  // Don't overwrite an existing config (e.g. hand-written, or generated by
  // create-shane-new-doc) so we don't clobber the user's settings
  if (fs.existsSync(configPath)) {
    return;
  }

  const config = {
    imgDir: imgDir || "public/img",
    lang,
  };

  fs.writeFileSync(configPath, JSON.stringify(config, null, 2) + "\n", "utf8");
  console.log(t("configCreated", { file: CONFIG_FILE }));
}

async function askImgDir() {
  if (!process.stdin.isTTY) {
    return "public/img";
  }

  try {
    return await input({
      message: t("askImgDir"),
      default: "public/img",
    });
  } catch {
    return "public/img";
  }
}

function writeScript(scriptsDir) {

  if (!fs.existsSync(scriptsDir)) {
    fs.mkdirSync(scriptsDir, { recursive: true });
    console.log(t("createdDir", { dir: path.relative(projectRoot, scriptsDir) }));
  }

  const existingName = findExistingScript(scriptsDir);
  const targetName = existingName || "new-doc.js";
  const targetPath = path.join(scriptsDir, targetName);

  if (isAlreadyManaged(targetPath)) {
    console.log(t("alreadyManagedSkip", { path: path.relative(projectRoot, targetPath) }));
    return targetPath;
  }

  const overwriting = fs.existsSync(targetPath);

  if (overwriting) {
    const backupPath = targetPath + ".bak";
    fs.copyFileSync(targetPath, backupPath);
    console.log(t("backedUp", { path: path.relative(projectRoot, backupPath) }));
  }

  const rawTemplate = fs.readFileSync(templatePath, "utf8");

  let content;
  if (rawTemplate.startsWith("#!")) {
    const newlineIndex = rawTemplate.indexOf("\n");
    content =
      rawTemplate.slice(0, newlineIndex + 1) +
      MANAGED_MARKER + "\n" +
      rawTemplate.slice(newlineIndex + 1);
  } else {
    content = MANAGED_MARKER + "\n" + rawTemplate;
  }

  fs.writeFileSync(targetPath, content, "utf8");

  console.log(
    overwriting
      ? t("tookOver", { path: path.relative(projectRoot, targetPath) })
      : t("generated", { path: path.relative(projectRoot, targetPath) })
  );

  return targetPath;
}

function injectPackageScript(pkg, scriptPath) {
  pkg.scripts ??= {};

  const relative = "node " + path.relative(projectRoot, scriptPath).replace(/\\/g, "/");

  if (pkg.scripts["new-doc"] === relative) {
    return;
  }

  pkg.scripts["new-doc"] = relative;

  fs.writeFileSync(pkgJsonPath, JSON.stringify(pkg, null, 2) + "\n", "utf8");

  console.log(t("scriptInjected", { cmd: relative }));
}

let t;

async function main() {

  const pkg = readProjectPackage();

  if (!pkg) {
    process.exit(0);
  }

  const existingConfig = readExistingConfig();
  const lang = await askLangIfNeeded({ config: existingConfig });
  t = makeT(packageDirFromImportMeta(import.meta.url), lang);

  if (isOwnRepository(pkg)) {
    console.log(t("ownRepoSkip"));
    process.exit(0);
  }

  if (!isStarlightProject(pkg)) {
    console.log(t("notStarlight"));
    process.exit(0);
  }

  const scriptsDir = path.join(projectRoot, "scripts");

  // If the script is already managed by this package, a full run already
  // happened before (or the config file was removed on purpose) -- don't
  // pop the interactive prompts again
  const existingName = fs.existsSync(scriptsDir) ? findExistingScript(scriptsDir) : null;
  const targetName = existingName || "new-doc.js";
  const targetPath = path.join(scriptsDir, targetName);

  if (isAlreadyManaged(targetPath)) {
    console.log(t("alreadyManagedSkip", { path: path.relative(projectRoot, targetPath) }));
    injectPackageScript(pkg, targetPath);
    process.exit(0);
  }

  const imgDir = await askImgDir();

  const scriptPath = writeScript(scriptsDir);
  writeConfig(imgDir, lang);

  injectPackageScript(pkg, scriptPath);
}

main();
